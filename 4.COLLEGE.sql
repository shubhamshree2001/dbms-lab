CREATE TABLE STUDENT(
USN VARCHAR(10) PRIMARY KEY,
SNAME VARCHAR(20),
ADDRESS VARCHAR(20),
PHONE NUMBER(10),
GENDER CHAR(1));

CREATE TABLE SEMSEC(
SSID VARCHAR(2) PRIMARY KEY,
SEM NUMBER(1),
SEC CHAR(1));

CREATE TABLE CLASS(
USN VARCHAR(10) REFERENCES STUDENT(USN) ON DELETE CASCADE,
SSID VARCHAR(2) REFERENCES SEMSEC(SSID) ON DELETE CASCADE,
PRIMARY KEY(USN,SSID));

CREATE TABLE SUBJECT(
SUBCODE VARCHAR(8) PRIMARY KEY,
TITLE VARCHAR(20),
SEM NUMBER(1),
CREDITS NUMBER(1));

CREATE  TABLE IAMARKS(
USN VARCHAR(10) REFERENCES STUDENT(USN) ON DELETE CASCADE,
SUBCODE VARCHAR(8) REFERENCES SUBJECT(SUBCODE) ON DELETE CASCADE,
SSID VARCHAR(2) REFERENCES SEMSEC(SSID) ON DELETE CASCADE,
TEST1 NUMBER(2) ,
TEST2 NUMBER(2),
TEST3 NUMBER(2),
FINALIA NUMBER(2),
PRIMARY KEY(USN,SUBCODE,SSID));


